class WorldGrid{
    constructor({level}){this.level = level;}
   
    loadMap(c){
        for(let row = 0 ; row < 28 ; row++){
            for(let col = 0 ; col < 60 ; col++){
                if(map1[row][col] == 0) continue;
                let tileObj = tiles[map1[row][col] -1];
                
                c.drawImage(
                    tyleSheet,
                    tileObj.x,
                    tileObj.y,
                    TILESIZE,
                    TILESIZE,
                    col*TILESIZE,
                    row*TILESIZE,
                    TILESIZE,
                    TILESIZE
                );
            }
        }
    }
}


